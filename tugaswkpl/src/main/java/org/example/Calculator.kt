
package org.example // Mendefinisikan paket tempat kelas Calculator berada

class Calculator { // Mendefinisikan kelas Calculator
    fun add(first: Int, second: Int): Int { // Fungsi penambahan, menerima dua parameter dan mengembalikan hasil penjumlahan
        return first + second // Mengembalikan hasil penjumlahan dari dua parameter
    }

    fun sub(first: Int, second: Int): Int { // Fungsi pengurangan, menerima dua parameter dan mengembalikan hasil pengurangan
        return first - second // Mengembalikan hasil pengurangan dari dua parameter
    }
}
